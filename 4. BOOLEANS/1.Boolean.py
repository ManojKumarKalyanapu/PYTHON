# Boolean Values
print(10 > 9)
print(10 == 9)
print(10 < 9)

# Evaluate Values and Variables
print(bool("Hello")) # true
print(bool(15)) # true
print(bool(["apple", "cherry", "banana"]))

# Some Values are False
print(bool(False))
print(bool(None))
print(bool(0))
print(bool(""))
print(bool(()))
print(bool([]))
print(bool({}))
