# BYTES
x = b"Hello"

#display x:
print(x)

#display the data type of x:
print(type(x)) 

# BYTEARRAY
x = bytearray(5)

#display x:
print(x)

#display the data type of x:
print(type(x)) 

# MEMORYVIEW
x = memoryview(bytes(5))

#display x:
print(x)

#display the data type of x:
print(type(x)) 

# setting specific data type
x = bytes(b"Hello")