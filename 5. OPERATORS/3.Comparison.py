# ✅ Comparison Operators Example in Python

a = 10
b = 20

# Equal to (==)
print("a == b:", a == b)  # False: because 10 is not equal to 20

# Not Equal to (!=)
print("a != b:", a != b)  # True: because 10 is not equal to 20

# Greater than (>)
print("a > b:", a > b)  # False: 10 is not greater than 20

# Less than (<)
print("a < b:", a < b)  # True: 10 is less than 20

# Greater than or equal to (>=)
print("a >= b:", a >= b)  # False: 10 is not greater than or equal to 20

# Less than or equal to (<=)
print("a <= b:", a <= b)  # True: 10 is less than or equal to 20


