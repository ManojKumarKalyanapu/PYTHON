# ✅ Simple Assignment
x = 5
print("Simple Assignment:", x)  # Output: 5


# ✅ Addition Assignment (+=)
x = 5
x += 3  # Same as x = x + 3
print("Addition Assignment:", x)  # Output: 8


# ✅ Subtraction Assignment (-=)
x = 5
x -= 3  # Same as x = x - 3
print("Subtraction Assignment:", x)  # Output: 2


# ✅ Multiplication Assignment (*=)
x = 5
x *= 3  # Same as x = x * 3
print("Multiplication Assignment:", x)  # Output: 15


# ✅ Division Assignment (/=)
x = 5
x /= 3  # Same as x = x / 3
print("Division Assignment:", x)  # Output: 1.6666666666666667


# ✅ Modulus Assignment (%=)
x = 5
x %= 3  # Remainder of x divided by 3
print("Modulus Assignment:", x)  # Output: 2


# ✅ Floor Division Assignment (//=)
x = 5
x //= 3  # Same as x = x // 3 (integer division)
print("Floor Division Assignment:", x)  # Output: 1


# ✅ Exponentiation Assignment (**=)
x = 5
x **= 3  # Same as x = x ** 3 (x raised to the power 3)
print("Exponentiation Assignment:", x)  # Output: 125


# ✅ Bitwise AND Assignment (&=)
x = 5
x &= 3  # Bitwise AND of x and 3
print("Bitwise AND Assignment:", x)  # Output: 1


# ✅ Bitwise OR Assignment (|=)
x = 5
x |= 3  # Bitwise OR of x and 3
print("Bitwise OR Assignment:", x)  # Output: 7


# ✅ Bitwise XOR Assignment (^=)
x = 5
x ^= 3  # Bitwise XOR of x and 3
print("Bitwise XOR Assignment:", x)  # Output: 6


# ✅ Right Shift Assignment (>>=)
x = 5 # 101
x >>= 3  # Shift bits of x to the right by 3, # if we move the bits to right then all becomes zero
print("Right Shift Assignment:", x)  # Output: 0


# ✅ Left Shift Assignment (<<=)
x = 5 # 101
x <<= 3  # Shift bits of x to the left by 3 , 101000
print("Left Shift Assignment:", x)  # Output: 40


# ✅ Walrus Operator (Assignment Expression :=)
print("Walrus Operator Assignment:", (x := 3))  # Assigns 3 to x and prints it
