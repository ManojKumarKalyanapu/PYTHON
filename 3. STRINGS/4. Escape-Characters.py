# Single Quote
txt = 'It\'s alright.'
print('signle-quote: ',txt) 

# Backslash
txt = "This will insert one \\ (backslash)."
print('Backslash: ',txt) 

# New Line
txt = "Hello\nWorld!"
print('New-Line: ',txt)

# Carriage Return
txt = "Hello\rWorld!"
print('Carriage: ',txt)

# Tab
txt = "Hello\tWorld!"
print('Tab: ',txt)

# Backspace
txt = "Hello\bWorld!"
print('Backspace: ',txt)

# Form Feed
txt = "Hello\fWorld!"
print('Fomr-feed: ',txt)

# Vertical Tab
txt = "Hello\vWorld!"
print('Vertical-Tab: ',txt)

# Octal Value
txt = "Hello \101 World!"  # Octal value for 'A'
print('Octal: ',txt)

# Hexadecimal Value
txt = "Hello \x41 World!"  # Hexadecimal value for 'A'
print('Hexadecimal: ',txt) 
     

